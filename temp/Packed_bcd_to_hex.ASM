  NAME MAIN
  SECTION datas:CONST(2)
NUM 
    DC32 0X1234
RESULT 
    DS32 1
  PUBLIC __iar_program_start
    SECTION .INTVEC:CODE:NOROOT(2)
__iar_program_start
MAIN
  LDR R0,=NUM
  MOV R5,#04
  MOV R9,#12
  MOV R1,#00
  EOR R2,R2,R2
  MOV R10,#10
  
  MOV R11,#0X0F

LOOP MOV R7,R1
  MUL R1,R7,R10
  LDRH R8,[R0]
  ROR R8,R8,R9
  AND R4,R8,R11
  ADD R1,R1,R4
  SUBS R9,R9,#04
  SUBS R5,R5,#01
  BNE LOOP
  
  STR R1,RESULT
STOP B STOP  
  END
  